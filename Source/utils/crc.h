/***************************************************************************//*!
*  \brief CRC 16bits rolling
*
*   This function is used to calculate the CRC 16bits of a buffer in rolling
*   mode.
*   
*   Preconditions: None.
*
*   Side Effects: None.
*
*   \param[in]  cksum               rolling checksum
*   \param[in]  buf                 Pointer to data buffer
*   \param[in]  len                 data buffer length
*
*   \return     operation status
*
*******************************************************************************/
uint16_t crc16_rolling(uint16_t cksum, uint8_t *buf, uint32_t len);

/***************************************************************************//*!
*  \brief CRC 16bits
*
*   This function is used to calculate the CRC 16bits of a buffer.
*   
*   Preconditions: None.
*
*   Side Effects: None.
*
*   \param[in]  buf                 Pointer to data buffer
*   \param[in]  len                 data buffer length
*
*   \return     operation status
*
*******************************************************************************/
uint16_t crc16(uint8_t *buf, uint32_t len);

